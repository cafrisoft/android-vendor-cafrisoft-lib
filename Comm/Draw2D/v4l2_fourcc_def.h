#define V4L2_MAX(a,b) ((a) > (b) ? (a) : (b))

#define V4L2_SWAP_I16(v) (short)((((v)>>8)  & 0xff) | (((v)&0xff) << 8))
#define V4L2_SWAP_I32(v) (int)((((v)>>24) & 0xff) | (((v)>>8) & 0xff00) | (((v)&0xff00) << 8) | (((v)&0xff) << 24))
#define V4L2_SWAP_U16(v) (unsigned short)((((v)>>8)  & 0xff) | (((v)&0xff) << 8))
#define V4L2_SWAP_U32(v) (unsigned int)((((v)>>24) & 0xff) | (((v)>>8) & 0xff00) | (((v)&0xff00) << 8) | (((v)&0xff) << 24))

#define V4L2_GETFOURCC(fourcc,ch) (unsigned char)(fourcc>>(8*ch))
#define V4L2_GETFOURCC_ARG(fourcc)  V4L2_GETFOURCC(fourcc,0),V4L2_GETFOURCC(fourcc,1),V4L2_GETFOURCC(fourcc,2),V4L2_GETFOURCC(fourcc,3)

/*****************************************************************************
      Pixel format          FOURCC                     depth  Description  
*****************************************************************************/

