/*****************************************************************************
     Byte Align
*****************************************************************************/

/*
    note : all component height align byte is 16 
*/

#define V4L2_BYTE_ALIGN(x, align)   (((x) + (align) - 1) & ~((align) - 1))


#define V4L2_VIDEO_FRAME_STRIDE_ALIGN(w)  V4L2_BYTE_ALIGN(w, 32)     /* MALI require chroma,16byte-align, so  Luma,align must be 32 */
#define V4L2_VIDEO_FRAME_HEIGHT_ALIGN(h)  V4L2_BYTE_ALIGN(h, 16)     /* VPU require  Luma,16byte-align , Chroma 8byte-align */
#define V4L2_VIDEO_STREAM_SIZE_ALIGN(sz)  V4L2_BYTE_ALIGN(sz, 1024) 

#define V4L2_IMAGE_FRAME_STRIDE_ALIGN(w)  V4L2_VIDEO_FRAME_STRIDE_ALIGN(w) //V4L2_BYTE_ALIGN(w, 16) 
#define V4L2_IMAGE_FRAME_HEIGHT_ALIGN(h)  V4L2_VIDEO_FRAME_HEIGHT_ALIGN(h) //V4L2_BYTE_ALIGN(h, 16)  
#define V4L2_IMAGE_STREAM_SIZE_ALIGN(sz) V4L2_VIDEO_STREAM_SIZE_ALIGN(sz) //V4L2_BYTE_ALIGN(streamsz, 1024)

